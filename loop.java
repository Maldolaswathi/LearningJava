class loop
{
    public static void main(String[] args)
    {
        for( int i=0;i<=100;i++)
        {
            System.out.println(i);
        }
            for(int j=1;j<100;j++)
            {
             System.out.println(j);
        }
        for(int k=0;k<=100;)
        {
            System.out.println(k);
            k=k+2;
        }
         for(int n=1;n<=100;)
        {
            System.out.println(n);
            n=n+2;
        }
    }
}


   